    private boolean tontai(int maKH, int maDV){
        for(int i=0; i<tmQL.getRowCount(); i++){
            if(Integer.parseInt(tmQL.getValueAt(i, 0).toString())==maKH
                    && Integer.parseInt(tmQL.getValueAt(i, 2).toString())==maDV){
                return true;
            }
        }
        return false;
    }
