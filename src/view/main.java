    private int demlansd(int maKH){
        int count=0;
        for(int i=0; i<tmQL.getRowCount(); i++){
            int ma1= Integer.parseInt(tmQL.getValueAt(i, 0).toString());
            if(ma1== maKH){
                count++;
            }
        }
        return count;
    }
    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        // TODO add your handling code here:
        if(themQL){
            int maKH= Integer.parseInt(jComboBox2.getSelectedItem().toString());
            int maDV= Integer.parseInt(jComboBox3.getSelectedItem().toString());
            if(!tontai(maKH, maDV)){
                if(demlansd(maKH)>5){
                    JOptionPane.showMessageDialog(null, "da qua 5 dich vu");
                    return;
                }
                else{
                    String tenkh= getKH(maKH).getHoten();
                    String tendv= getDV(maDV).getTen();
                    int sl= Integer.parseInt(jTextField8.getText());
                    
                    QuanLy q= new QuanLy(maKH, tenkh, maDV, tendv, sl);
                    
                    tmQL.addRow(new Object[]{maKH, tenkh, maDV, tendv, sl});
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "da ton tai");
                    return;
            }
        }
    }//GEN-LAST:event_jButton12ActionPerformed
